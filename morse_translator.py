class MorseTranslator:
    def str_to_morse(self, text):
        code = " ".join([STR_MORSE_DICT[char] for char in text.lower() if char in STR_MORSE_DICT])
        return code

    def morse_to_str(self, code):
        text = "".join([MORSE_STR_DICT[char] for char in code.split() if char in MORSE_STR_DICT])
        return text
